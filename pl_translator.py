def pig_latin(text):
  #Separate the text into words
  words = text.split(" ")
  pl_words = []
  for word in words:
    # Create the pig latin word and add it to the list
    word = word[1:] + word[0] + "ay"
    pl_words.append(word)
  # Turn the list back into a phrase
  new_text = " ".join(pl_words)
  return new_text
